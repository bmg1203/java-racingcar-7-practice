package racingcar.view;

public class Data {
    public String carNames;
    public int trialNum;

    public Data(String carNames, int trialNum) {
        this.carNames = carNames;
        this.trialNum = trialNum;
    }

    public String getCarNames() {
        return carNames;
    }

    public int getTrialNum() {
        return trialNum;
    }

}
