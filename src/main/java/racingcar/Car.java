package racingcar;

import camp.nextstep.edu.missionutils.Randoms;

public class Car {
    public String name;
    public int score;

    // Car 객체 생성
    public Car() {
        this.name = name;
        this.score = 0;
    }

    // 점수 반환
    public int getScore() {
        return score;
    }

    // 차 이름 반환
    public String getName() {
        return name;
    }
}
